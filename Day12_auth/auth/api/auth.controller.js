/*
  ORIGINAL COMMENTS:

  registration controller
  check email exits
      if true: 401 {success:'false',user already exists}
      else:
          hash the password using bcryptjs rounds 10:
          create user in db (but allocate uuid on server)
          return 201 {success:'true',message:'user created'}

  login controller
  check user exists with email
      if false: 401 {success:false, message:"invalid credential"}
      else:
          compare the hashed password
          if not match : 404 {success:false, message:"user not found"}
          else:
              match the password
              if password does not match:
                  401 {success:false,message:"invalid credential"}
              else:
                  generate access token with 15 min expiry using jwt
                  set access token in http only cookie secure true same site: strict/lax
                  return 200 {success:true,message:"login successful",accessToken}

  logout controller
  clear access token cookie http only cookie secure true same site: strict/lax
  return 200 {success:true,message:"logout successful"}

  IMPLEMENTATION:
  - register: checks if email exists via drizzle, hashes password, inserts user
  - login: finds user by email, compares bcrypt hash, signs JWT, sets httpOnly cookie
  - logout: clears the accessToken cookie with matching options
*/
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import { eq } from "drizzle-orm";
import { db } from "../../config/db-congid.js";
import { users } from "../../schema/user.model.js";

// ───────────────── Registration Controller ─────────────────
export const register = async (req, res) => {
  try {
    const { email, password } = req.body;

    // Check if email already exists
    const existingUser = await db
      .select()
      .from(users)
      .where(eq(users.email, email))
      .limit(1);

    if (existingUser.length > 0) {
      return res.status(401).json({
        success: false,
        message: "User already exists",
      });
    }

    // Hash the password using bcryptjs with 10 rounds
    const hashedPassword = await bcrypt.hash(password, 10);

    // Create user in db (UUID is generated by the database via defaultRandom)
    await db.insert(users).values({
      email,
      password: hashedPassword,
    });

    return res.status(201).json({
      success: true,
      message: "User created",
    });
  } catch (error) {
    console.error("Registration error:", error);
    return res.status(500).json({
      success: false,
      message: "Internal server error",
    });
  }
};

// ───────────────── Login Controller ─────────────────
export const login = async (req, res) => {
  try {
    const { email, password } = req.body;

    // Check if user exists with email
    const existingUsers = await db
      .select()
      .from(users)
      .where(eq(users.email, email))
      .limit(1);

    if (existingUsers.length === 0) {
      return res.status(401).json({
        success: false,
        message: "Invalid credential",
      });
    }

    const user = existingUsers[0];

    // Compare the hashed password
    const isPasswordValid = await bcrypt.compare(password, user.password);

    if (!isPasswordValid) {
      return res.status(401).json({
        success: false,
        message: "Invalid credential",
      });
    }

    // Generate access token with 15 min expiry
    const accessToken = jwt.sign(
      { id: user.id, email: user.email },
      process.env.JWT_SECRET,
      { expiresIn: "15m" }
    );

    // Set access token in httpOnly cookie, secure, sameSite: lax
    res.cookie("accessToken", accessToken, {
      httpOnly: true,
      secure: true,
      sameSite: "lax",
      maxAge: 15 * 60 * 1000, // 15 minutes in ms
    });

    return res.status(200).json({
      success: true,
      message: "Login successful",
      accessToken,
    });
  } catch (error) {
    console.error("Login error:", error);
    return res.status(500).json({
      success: false,
      message: "Internal server error",
    });
  }
};

// ───────────────── Logout Controller ─────────────────
export const logout = (req, res) => {
  // Clear the accessToken cookie with matching options
  res.clearCookie("accessToken", {
    httpOnly: true,
    secure: true,
    sameSite: "lax",
  });

  return res.status(200).json({
    success: true,
    message: "Logout successful",
  });
};
